          <p class="square_p">Enhanced Oil Recovery or EOR is a series of technologies that are used to improve amount of oil that can be recovered from oil bearing formations. Oil can be extracted in three phases: primary recovery, secondary recovery and tertiary recovery. During primary recovery, natural drive energy will be used, including natural aquifers influx, solution gas, gas cap, gravity drainage, or a combination of these drive energies. Secondary recovery process involves injection of water or gases to maintain/raise reservoir pressure. Primary and secondary recovery processes are known leaving two thirds of original oil in place (OOIP) underground. The remaining oil is located in non-accessible reservoir regions or pores due to capillary pressure and wettability issues. Tertiary recovery process, also called EOR, is to recover the remaining oil using variation of injection agents.</p>
